class Producto {
    constructor(id, name, description, price, img, stock, cantidad, categoria) {
        this.id = id;
        this.name = name;
        this.description = description;
        this.price = price;
        this.img = img;
        this.stock = stock;
        this.cantidad = cantidad;
        this.categoria = categoria;
    }
}
export {Producto}